package es.inteco.rastreador2.utils;

public class TerminoCatVer {
    private String termino;
    private String id_termino;
    private String porcentaje;
    private String porcentajeNorm;

    public String getTermino() {
        return termino;
    }

    public void setTermino(String termino) {
        this.termino = termino;
    }

    public String getId_termino() {
        return id_termino;
    }

    public void setId_termino(String id_termino) {
        this.id_termino = id_termino;
    }

    public String getPorcentaje() {
        return porcentaje;
    }

    public void setPorcentaje(String porcentaje) {
        this.porcentaje = porcentaje;
    }

    public String getPorcentajeNorm() {
        return porcentajeNorm;
    }

    public void setPorcentajeNorm(String porcentajeNorm) {
        this.porcentajeNorm = porcentajeNorm;
    }


}
