package es.inteco.rastreador2.dao.rastreo;

/**
 * The Class GlobalReportStatistics.
 */
public class GlobalReportStatistics {
	/** The count. */
	private Integer count;
	/** The nombre. */
	private String nombre;
	/** The id clasificacion. */
	private Integer idClasificacion;
	/** The nombre clasificacion. */
	private String nombreClasificacion;

	/**
	 * Gets the nombre clasificacion.
	 *
	 * @return the nombreClasificacion
	 */
	public String getNombreClasificacion() {
		return nombreClasificacion;
	}

	/**
	 * Sets the nombre clasificacion.
	 *
	 * @param nombreClasificacion the nombreClasificacion to set
	 */
	public void setNombreClasificacion(String nombreClasificacion) {
		this.nombreClasificacion = nombreClasificacion;
	}

	/**
	 * Gets the count.
	 *
	 * @return the count
	 */
	public Integer getCount() {
		return count;
	}

	/**
	 * Sets the count.
	 *
	 * @param count the count to set
	 */
	public void setCount(Integer count) {
		this.count = count;
	}

	/**
	 * Gets the nombre.
	 *
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}

	/**
	 * Sets the nombre.
	 *
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	/**
	 * Gets the id clasificacion.
	 *
	 * @return the idClasificacion
	 */
	public Integer getIdClasificacion() {
		return idClasificacion;
	}

	/**
	 * Sets the id clasificacion.
	 *
	 * @param idClasificacion the idClasificacion to set
	 */
	public void setIdClasificacion(Integer idClasificacion) {
		this.idClasificacion = idClasificacion;
	}
}